#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	if (s1 == 0 || s2 == 0)
		return (0);
	i = 0;
	if ((s1[i] == 0 && s2[i] == 0) || n == 0)
		return (1);
	while ((s1[i] != '\0' || s2[i] != '\0') && i < n)
	{
		if (s1[i] == s2[i])
		{
			i++;
			if ((s1[i] == '\0' && s2[i] == '\0') || n == i)
				return (1);
		}
		if (s1[i] != s2[i])
			return (0);
	}
	return (0);
}
